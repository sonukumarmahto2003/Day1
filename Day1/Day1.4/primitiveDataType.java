public class primitiveDataType {
    public static void main(String[] args) {
        byte b = 8;
        System.out.println(b);
        char ch = 'a';
        System.out.println(ch);
        boolean var = true;
        System.out.println(var);
        float price = 10.5f;
        System.out.println(price);
        int nuber = 25;
        System.out.println(nuber);
        // long
        // double
        short n = 240;
        System.out.println(n);
    }
}
